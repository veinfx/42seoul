int				ft_sqrt(int nb)
{
	long long	i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i * i <= (long)nb)
	{
		if (i * i == (long)nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
