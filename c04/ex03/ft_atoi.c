int		ft_atoi(char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		++str;
	while (*str == '+' || *str == '-')
		if (*(str++) == '-')
			k *= -1;
	while (*str >= '0' && *str <= '9')
	{
		i *= 10;
		i += (*(str++) - '0');
	}
	i *= k;
	return (i);
}
